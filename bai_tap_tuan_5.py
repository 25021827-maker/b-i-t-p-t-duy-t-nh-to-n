def isomorphic(a, b):
    if len(a) != len(b):
        return False

    map_a_to_b = {}
    map_b_to_a = {}

    for char_a, char_b in zip(a, b):
        # Kiểm tra ánh xạ a -> b
        if char_a in map_a_to_b:
            if map_a_to_b[char_a] != char_b:
                return False
        else:
            map_a_to_b[char_a] = char_b


        if char_b in map_b_to_a:
            if map_b_to_a[char_b] != char_a:
                return False
        else:
            map_b_to_a[char_b] = char_a

    return True



print(isomorphic("egg", "add"))
print(isomorphic("paper", "title"))    # true
print(isomorphic("foo", "bar"))        # false
print(isomorphic("show", "same"))      # true
print(isomorphic("show", "seem"))      # false