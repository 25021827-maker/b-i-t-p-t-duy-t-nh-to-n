def may_tinh_bo_tui(num1, num2, operator):
    if operator == '+':
        result = num1 + num2
    elif operator == '-':
        result = num1 - num2
    elif operator == '*':
        result = num1 * num2
    elif operator == '/':
        result = num1 / num2
    elif operator == '%':
        result = num1 % num2
    elif operator == '//':
        result = num1 // num2
    else:
        return "Toán tử không hợp lệ"

    return round(result, 2)
print(may_tinh_bo_tui(2, 3, '+'))      # 5.0
